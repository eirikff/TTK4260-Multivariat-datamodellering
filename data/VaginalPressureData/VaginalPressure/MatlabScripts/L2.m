function fCost = L2(	afParametersGuess,			...
						afMeasuredPressureLevels,	...
						abMeasuredActivityZones		)
	%
	% create the structure
	tModel = CreateModelFromParameters( afParametersGuess );
	%
	% perform the simulations
	[	afSimulatedFatigued,					...
		afSimulatedActive,						...
		afSimulatedRested	]	=				...
			Simulate(							...
				tModel,							...
				0,								...
				afMeasuredPressureLevels(1),	...
				abMeasuredActivityZones 		);
	%
	% compute the cost
	fCost = sum( ( afSimulatedActive - afMeasuredPressureLevels ).^2 );
	%
end % function

